/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode() : val(0), left(nullptr), right(nullptr) {}
 *     TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
 *     TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left), right(right) {}
 * };
 */
struct TreeNode {
    int val;
    TreeNode *left;
    TreeNode *right;

    TreeNode():
      val(0),
      left(nullptr),
      right(nullptr) {}

    explicit TreeNode(int x):
      val(x),
      left(nullptr),
      right(nullptr) {}

    TreeNode(int x, TreeNode *left, TreeNode *right):
      val(x),
      left(left),
      right(right) {}
};

class Solution {
    int ans = 0;

  public:
    int goodNodes(TreeNode *root) {
        inner(root, std::numeric_limits<int>::min());
        return ans;
    }

    void inner(TreeNode *root, int cur) {
        if (!root) {
            return;
        }
        if (root->val >= cur) {
            cur = root->val;
            ans++;
        }
        inner(root->left, cur);
        inner(root->right, cur);
    }
};